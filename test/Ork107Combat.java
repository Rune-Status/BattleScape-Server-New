        combat.id(NpcId.ORK_107);
        combat.hitpoints(NpcCombatHitpoints.total(110));
        combat.stats(NpcCombatStats.builder().attackLevel(100).defenceLevel(60).bonus(CombatBonus.MELEE_ATTACK, 19).bonus(CombatBonus.DEFENCE_STAB, 12).bonus(CombatBonus.DEFENCE_SLASH, 14).bonus(CombatBonus.DEFENCE_CRUSH, 13).bonus(CombatBonus.DEFENCE_MAGIC, 5).bonus(CombatBonus.DEFENCE_RANGED, 13).build());
        combat.aggression(NpcCombatAggression.PLAYERS);
        combat.combatScript("BandosGWDCS").deathAnimation(4321).blockAnimation(4322);
        combat.drop(drop.build());

        var style = NpcCombatStyle.builder();
        style.type(NpcCombatStyleType.melee(CombatBonus.ATTACK_CRUSH));
        style.damage(NpcCombatDamage.maximum(12));
        style.animation(4320).attackSpeed(5);
        style.projectile(NpcCombatProjectile.id(335));
        combat.style(style.build());


        return Arrays.asList(combat.build());
